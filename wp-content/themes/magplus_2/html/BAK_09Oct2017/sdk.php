<div class="mbox" id="SDK_Tech">
  <div class="mbox-solid mbox-top-line mshow mbox-top-line-black mtext group">
    <h3 class="textleft"><strong>Mag+ SDK deliverables</strong></h3>
    <p>
      The following components are included in the SDK package.
    </p>
    <div class="mbox3-inside">
      <h3>The Reader</h3>
      <p>(MIBKit)</p>
      <p>
        The Mag+ iOS SDK consists of over 
        40 Objective-C classes and protocols 
        for parsing, rendering and interacting 
        with MIB files created using the Mag+ 
        tools. 
      </p>
      <p>
        MIBKit is designed as a first class UIKit citizen, adhering to Cocoa Touch 
        convention and best practises. 
      </p>
      <p>
        MIBKit is ready for both low level 
        customisation and high level convenience, leveraging Core Animation for 
        high performance, and the latest iOS 
        features for sharing content and media playback.
      </p>
      <p>
        Think of MIBKit as your WebKit for 
        high production value Mag+ content.
      </p>
    </div>
    <div class="mbox3-inside">
      <h3>Connector</h3>
      <p>(DennisKit)</p>
      <p>
        The Mag+ iOS SDK includes nonblocking asynchronous APIs for accessing the Mag+ publishing platform. 
      </p>
      <p>
        Including accessing issues, entitlements, subscriptions and application 
        run-time configuration settings. 
      </p>
      <p>
        As well as robust content download, 
        including background Newsstand 
        downloads, and integration with 
        StoreKit and third party subscription 
        services.
      </p>
    </div>
    <div class="mbox3-inside">
      <h3>Docs &amp; Samples</h3>
      <p>&nbsp;</p>
      <p>
        Included in the Mag+ iOS SDK is fully 
        integrated documentation as Xcode 
        docsets, for inline and searchable help. 
      </p>
      <p>
        Small, and to the point, example projects are included to get you started 
