    <h2>A regular Mag+ app vs a SDK custom app?</h2>
    <p>
      Here are just a few of the things you 
      can do with a custom app you can’t do 
      with a regular Mag+ app.
    </p>
    <p>
      <strong>Change the menu.</strong> With the Mag+ SDK, you 
      build your own menu, so you can add new options, like a shopping cart or custom buttons. 
      And the menu can be anywhere you want and 
      look however you want.
    </p>
    <p>
      <strong>Customize the store and library.</strong> Want a different storefront? With the SDK, you can build 
      your own store and library interfaces and decide 
      what goes in them. You can even skip both all 
      together and just bundle your issue into the 
      app.
    </p>
    <p>
      <strong>Add third-party services.</strong> Want Google analytics in your app? Or Facebook’s OpenGraph 
      API? With the SDK, you can build whatever 
      third-party services you want into your app.
    </p>
  </div>
  <div class="mhalf">
    <img src="<?php echo get_template_directory_uri(); ?>/html/images/SDK_Regular_vs_SDK_2jn.jpg" alt="Regular vs SDK" class="imgwide">
    <p>
      <strong>Change the navigation.</strong> The SDK includes 
      some of the basic Mag+ features, like the 
      thumbnail scrubber and the page-length indicator, but you can delete these, change them, 
      or build your own. You can even create your 
      own control gestures.
    </p>
    <p>
      <strong>Embed the Mag+ reader in another app.</strong> Already have an app—maybe a game, tool or 
      catalog? With the SDK, you can put the Mag+ 
      reader inside that app, so reading your content 
      becomes just another feature of your app.
    </p>
  </div>
